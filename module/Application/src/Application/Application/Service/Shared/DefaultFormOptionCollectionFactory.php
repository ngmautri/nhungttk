<?php
namespace Application\Application\Service\Shared;

use Application\Infrastructure\Persistence\Application\Doctrine\AppCollectionRepositoryImpl;
use Zend\ServiceManager\FactoryInterface;
use Zend\ServiceManager\ServiceLocatorInterface;

/**
 *
 * @author Nguyen Mau Tri
 *
 */
class DefaultFormOptionCollectionFactory implements FactoryInterface
{

    /**
     *
     * {@inheritdoc}
     * @see \Zend\ServiceManager\FactoryInterface::createService()
     */
    public function createService(ServiceLocatorInterface $serviceLocator)
    {
        $container = $serviceLocator;
        $service = new DefaultFormOptionCollection();

        $sv = $container->get(AppCollectionRepositoryImpl::class);
        $service->setAppCollectionRepository($sv);

        $sv = $container->get('AppLogger');
        $service->setLogger($sv);

        $sv = $container->get('AppCache');
        $service->setCache($sv);

        return $service;
    }
}